

public class Operacoes {
    public static int Adicao(int n1, int n2){
        return n1 + n2;
    }

    public static int Subtracao(int n1, int n2){
        return n1 - n2;
    }

    public static int Multiplicacao(int n1, int n2){
        return n1 * n2;
    }

    public static int Divisao(int n1, int n2){
        return n1 / n2;
    }
}
